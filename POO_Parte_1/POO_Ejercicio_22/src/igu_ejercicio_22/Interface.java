    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        
        try {
            
            String name = nameField.getText();
            double price = Double.parseDouble(salaryField.getText());
            int hours = Integer.parseInt(hoursField.getText());
            
            // Crear un objeto de tipo Empleado
            Empleado empleado = new Empleado(name, price, hours);

            CardLayout cardLayout = (CardLayout) panelCardLayout.getLayout();
            cardLayout.show(panelCardLayout, "card3");

            double value = 450000;
            
            if (empleado.comparateSalary(value)) {
            
                double salary = empleado.getSalary();
                salaryResult.setText(Double.toString(salary));
                comparateResult.setText("> " + Double.toString(value));
                
            } else {
            
                comparateResult.setText("< " + Double.toString(value));
            }
            
            nameResult.setText(name);
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los valores ingresados no son vÃ¡lidos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_calculateButtonActionPerformed

